    {
        $transaction = $request->isMethod('put')? Transaction::findOrFail($request->transaction_id): new Transaction;
        $transaction->id = $request->input('transaction_id');
        $transaction->result = $request->input('result');
        $transaction->issuer = $request->input('issuer');
        $transaction->nameOnCard = $request->input('nameOnCard');
        $transaction->number = $request->input('number');

        if($transaction->save()){
            return $transaction;
        }
    }

